#include "cgpsinterface.h"
#include "st.h"
#include <QtGlobal>
namespace gps
{
// --------------------------------------------------------------------------------------------------------------------------------------
CGpsInterface::CGpsInterface()
{
    m_gps = NULL;
}
// --------------------------------------------------------------------------------------------------------------------------------------
CGpsInterface::CGpsInterface(CGpsSource *gps)
{
    setGps(gps);
}
// --------------------------------------------------------------------------------------------------------------------------------------
CGpsInterface::CGpsInterface(const TGpsSourceId &id)
{
    setGps(id);
}
// --------------------------------------------------------------------------------------------------------------------------------------
CGpsInterface::~CGpsInterface()
{
}
// --------------------------------------------------------------------------------------------------------------------------------------
CGpsSource * CGpsInterface::gps()
{
    Q_CHECK_PTR(m_gps);
    return m_gps;
}
// --------------------------------------------------------------------------------------------------------------------------------------
void CGpsInterface::setGps(CGpsSource *value)
{
    m_gps = value;
}
// --------------------------------------------------------------------------------------------------------------------------------------
void CGpsInterface::setGps(const TGpsSourceId &value)
{
    m_gps = GT_GPS_MANAGER->source(value);
}

}
